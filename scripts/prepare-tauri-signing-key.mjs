import { writeFile } from "node:fs/promises"

function isRequired(value) {
  return !/^(0|false|no)$/i.test(value ?? "true")
}

function stripWrappingQuotes(value) {
  return value.replace(/^['"]|['"]$/g, "")
}

function looksLikeUpdaterKey(text) {
  return /untrusted comment:\s*(minisign secret key|rsign encrypted secret key)/i.test(text)
}

function normalizeKey(rawValue) {
  const trimmed = stripWrappingQuotes(rawValue.trim())

  if (!trimmed) {
    return null
  }

  if (trimmed.startsWith("-----BEGIN")) {
    throw new Error(
      "RAW_TAURI_SIGNING_PRIVATE_KEY looks like a PEM key. Use the updater key generated by `tauri signer generate`."
    )
  }

  const collapsed = trimmed.replace(/\s+/g, "")
  if (/^[A-Za-z0-9+/=]+$/.test(collapsed)) {
    const decoded = Buffer.from(collapsed, "base64").toString("utf8")
    if (looksLikeUpdaterKey(decoded)) {
      return collapsed
    }
  }

  if (looksLikeUpdaterKey(trimmed)) {
    return Buffer.from(trimmed, "utf8").toString("base64")
  }

  throw new Error(
    "RAW_TAURI_SIGNING_PRIVATE_KEY must contain either the base64 key from `tauri signer generate` or the raw key file contents."
  )
}

async function main() {
  const outputPath = process.env.TAURI_SIGNING_KEY_OUTPUT_PATH
  const rawKey = process.env.RAW_TAURI_SIGNING_PRIVATE_KEY ?? ""
  const required = isRequired(process.env.TAURI_SIGNING_KEY_REQUIRED)
  const normalizedKey = normalizeKey(rawKey)

  if (!normalizedKey) {
    if (required) {
      throw new Error("Missing RAW_TAURI_SIGNING_PRIVATE_KEY.")
    }

    console.log("No updater signing key provided.")
    return
  }

  if (!outputPath) {
    throw new Error("Missing TAURI_SIGNING_KEY_OUTPUT_PATH.")
  }

  await writeFile(outputPath, normalizedKey, "utf8")
  console.log(`Prepared updater signing key at ${outputPath}`)
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error)
  process.exitCode = 1
})
